    static func  GenerateBoard2 () -> [[Character]]{
        
        let path = Bundle.main.path(forResource: "board-2-3", ofType: "txt")
        var data = ""
        do{
            data = try String(contentsOfFile: path!)
        } catch{
            
        }
        
        print(data)
        var board: [[Character]] = [[]]
        var i = 0
        for char in data {
            if(char == "\u{000A}")
            {
                board.append([])
                i+=1
            }else{
                board[i].append(char)
            }
            
        }
        board.popLast()
        return board
    }
    
    static func FindStart(_ board : [[Character]]) -> NSPoint{
        for x in 0...board.count - 1{
            for y in 0...board[x].count - 1{
                if(board[x][y] == "A"){
                    return NSPoint(x:x, y:y)
                }
            }
        }
        return NSPoint()
    }
    
    static func FindEnd(_ board : [[Character]]) -> NSPoint{
        for x in 0...board.count - 1{
            for y in 0...board[x].count - 1{
                if(board[x][y] == "B"){
                    return NSPoint(x:x, y:y)
                }
            }
        }
        
        return NSPoint()
    }
